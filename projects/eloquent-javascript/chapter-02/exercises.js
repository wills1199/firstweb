function triangles(number) {
    for (let i = 1; i <= number; i++){
        console.log("#".repeat(i));
    }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(num1, num2) {
    for (let i = num1; i <= num2; i++){
        if ((i % 3 === 0) && (i % 5 === 0)){
            console.log("fizzbuzz"); 
        } else if (i % 3 === 0){
            console.log("fizz");
        } else if (i % 5 === 0){
            console.log("buzz");
        } else {
            console.log(i);
        }
    }  
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


function drawChessboard(num) {
  let board = "";
  for (let i = 0; i < num; i++){
    for (let a = 0; a < num; a++){
      if((a % 2) === (i % 2)){
        board += " "
      } else {
        board +=  "#"
      }
    }
    board += "\n";
  }
  console.log(board);
}


